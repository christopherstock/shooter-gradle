
    package de.christopherstock.lib;

    /*******************************************************************************************************************
    *   Represents an offset in 3D space.
    *******************************************************************************************************************/
    public final class LibOffset
    {
        public              float                   x               = 0.0f;
        public              float                   y               = 0.0f;
        public              float                   z               = 0.0f;

        public LibOffset( float x, float y, float z )
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
